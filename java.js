function large(img) {
    img.style.padding = "1.5vw 3vw"
    img.style.fontSize = "3vw"
    img.style.borderRadius = ".75vw"
    img.style.transition = "500ms"
}

function normal(img) {
    img.style.padding = "1vw 2vw"
    img.style.fontSize = "2vw"
    img.style.borderRadius = ".5vw"
    img.style.transition = "500ms"
}